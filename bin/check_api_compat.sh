#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
SNAPSHOT="${ROOT_DIR}/docs/api/exports.txt"
TMP_DIR="$(mktemp -d)"
trap 'rm -rf "${TMP_DIR}"' EXIT

GENERATOR="${TMP_DIR}/api_snapshot.go"
CURRENT="${TMP_DIR}/exports.txt"

cat >"${GENERATOR}" <<'GO'
package main

import (
	"fmt"
	"go/ast"
	"go/parser"
	"go/token"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"golang.org/x/tools/go/packages"
)

const modulePath = "github.com/imajinyun/go-knifer"

type packageInfo struct {
	ImportPath string
	Dir        string
	GoFiles    []string
}

func main() {
	pkgs, err := packages.Load(&packages.Config{Mode: packages.NeedName | packages.NeedFiles | packages.NeedImports}, "./...")
	if err != nil {
		fatal(err)
	}

	packages := make([]packageInfo, 0, len(pkgs))
	for _, pkg := range pkgs {
		if len(pkg.Errors) > 0 {
			fatal(pkg.Errors[0])
		}
		if pkg.Name == "main" || len(pkg.GoFiles) == 0 {
			continue
		}
		if !isPublicAPIPackage(pkg.PkgPath) {
			continue
		}

		info := packageInfo{ImportPath: pkg.PkgPath}
		seenDir := ""
		for _, file := range pkg.GoFiles {
			dir, name := filepath.Split(file)
			if strings.HasSuffix(name, "_test.go") {
				continue
			}
			if seenDir == "" {
				seenDir = strings.TrimSuffix(dir, string(filepath.Separator))
				info.Dir = seenDir
			}
			if strings.TrimSuffix(dir, string(filepath.Separator)) == seenDir {
				info.GoFiles = append(info.GoFiles, name)
			}
		}
		if len(info.GoFiles) > 0 {
			packages = append(packages, info)
		}
	}
	sort.Slice(packages, func(i, j int) bool { return packages[i].ImportPath < packages[j].ImportPath })

	lines := []string{
		"# Exported API Snapshot",
		"# Generated by bin/check_api_compat.sh. Run UPDATE_API=1 make api-check after intentional public API changes.",
		"# Scope: module root and top-level v* facade packages only.",
		"",
	}

	for _, pkg := range packages {
		if pkg.ImportPath == "" || len(pkg.GoFiles) == 0 {
			continue
		}

		symbols, err := exportedSymbols(pkg)
		if err != nil {
			fatal(err)
		}
		if len(symbols) == 0 {
			continue
		}

		lines = append(lines, pkg.ImportPath)
		for _, symbol := range symbols {
			lines = append(lines, "  "+symbol)
		}
		lines = append(lines, "")
	}

	_, err = fmt.Fprint(os.Stdout, strings.Join(lines, "\n"))
	if err != nil {
		fatal(err)
	}
}

func exportedSymbols(pkg packageInfo) ([]string, error) {
	fset := token.NewFileSet()
	symbols := make(map[string]struct{})
	for _, name := range pkg.GoFiles {
		file, err := parser.ParseFile(fset, filepath.Join(pkg.Dir, name), nil, 0)
		if err != nil {
			return nil, err
		}

		for _, decl := range file.Decls {
			switch d := decl.(type) {
			case *ast.FuncDecl:
				if d.Recv == nil && d.Name.IsExported() {
					symbols["func "+d.Name.Name] = struct{}{}
				}
			case *ast.GenDecl:
				kind := strings.ToLower(d.Tok.String())
				for _, spec := range d.Specs {
					switch s := spec.(type) {
					case *ast.TypeSpec:
						if s.Name.IsExported() {
							symbols["type "+s.Name.Name] = struct{}{}
						}
					case *ast.ValueSpec:
						for _, name := range s.Names {
							if name.IsExported() {
								symbols[kind+" "+name.Name] = struct{}{}
							}
						}
					}
				}
			}
		}
	}

	out := make([]string, 0, len(symbols))
	for symbol := range symbols {
		out = append(out, symbol)
	}
	sort.Strings(out)
	return out, nil
}

func isPublicAPIPackage(importPath string) bool {
	if importPath == modulePath {
		return true
	}
	rest, ok := strings.CutPrefix(importPath, modulePath+"/")
	if !ok || strings.Contains(rest, "/") {
		return false
	}
	return strings.HasPrefix(rest, "v")
}

func fatal(err error) {
	_, _ = fmt.Fprintln(os.Stderr, err)
	os.Exit(1)
}
GO

(cd "${ROOT_DIR}" && go run "${GENERATOR}") >"${CURRENT}"

if [[ "${UPDATE_API:-}" == "1" ]]; then
	mkdir -p "$(dirname "${SNAPSHOT}")"
	cp "${CURRENT}" "${SNAPSHOT}"
	echo "updated ${SNAPSHOT#"${ROOT_DIR}/"}"
	exit 0
fi

if [[ ! -f "${SNAPSHOT}" ]]; then
	echo "missing ${SNAPSHOT#"${ROOT_DIR}/"}; run UPDATE_API=1 make api-check" >&2
	exit 1
fi

if ! diff -u "${SNAPSHOT}" "${CURRENT}"; then
	echo "exported API snapshot is stale; run UPDATE_API=1 make api-check after intentional API changes" >&2
	exit 1
fi

echo "exported API snapshot is current"
