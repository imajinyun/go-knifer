// getGlyph returns the bitmap for ch; lowercase letters are normalized to
// uppercase, and unknown characters fall back to '?'.
func getGlyph(ch byte) [7]byte {
	if ch >= 'a' && ch <= 'z' {
		ch = ch - 'a' + 'A'
	}
	if g, ok := glyphs[ch]; ok {
		return g
	}
	return glyphs['?']
}
