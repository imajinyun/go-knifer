// getGlyph 返回字符的位图；小写字母转为大写处理；
// 未识别字符回退为 '?'。
func getGlyph(ch byte) [7]byte {
	if ch >= 'a' && ch <= 'z' {
		ch = ch - 'a' + 'A'
	}
	if g, ok := glyphs[ch]; ok {
		return g
	}
	return glyphs['?']
}
